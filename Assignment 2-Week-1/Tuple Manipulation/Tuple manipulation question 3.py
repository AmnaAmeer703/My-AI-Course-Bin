# Swap two tuples in python

tuple1 = ("Amna","Zainab","Rashid","Shama","Usman","Salman")
tuple2 = ("Javeria","Sarah","Fatima","Hassan","Farwa")

print("Orignal tuples are: ")
print("tuple1: ", tuple1)
print("tuple2: ", tuple2)

# Now swap the tuples
tuple1,tuple2 = tuple2, tuple1

print("New Swapped tuples are: ")
print("tuple1: ", tuple1)
print("tuple2: ", tuple2)